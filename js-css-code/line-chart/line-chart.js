function addCircles(id, team_color)	{
	
	/* Add circles in the line */
	
	var o = data[0];
	
	for (var i = 0; i < data.length; i++)	{
		if (data[i]['id'] == id)	{
			o = data[i];
		}
	}
	
	/*d3.selectAll('.lines')
	.selectAll('.line-group')
	.select('#' + id)).data()*/
	
	
	d3.selectAll('.lines')
	.selectAll('.line-group')
	.select('#' + id)
	.data([o]).enter()
	.append("g")
	.attr("class", id + "1")
	// .style("fill", (d, i) => color(i))
	.selectAll("circle")
	.data(d => d.values).enter()
	.append("g")
	.attr("class", "circle")
	.attr("class", id + "1")
	.on("mouseover", function(d) {
		  if (d.rank < 215)	{
		  d3.select(this)     
			.style("cursor", "pointer")
			.append("text")
			.attr("class", "text")
			.text(`${d.rank}`)
			.attr("x", d => xScale(d.date) + 5)
			.attr("y", d => yScale(d.rank) - 10);
		  }
		  else	{
			  d3.select(this)     
			.style("cursor", "pointer")
			.append("text")
			.attr("class", "text")
			.text("not ranked")
			.attr("x", d => xScale(d.date) - 8)
			.attr("y", d => yScale(d.rank) - 10);
		  }
		})
	  .on("mouseout", function(d) {
		  d3.select(this)
			.style("cursor", "none")  
			.transition()
			.duration(duration)
			.selectAll(".text").remove();
		})
	  .append("circle")
	  .attr("cx", d => xScale(d.date))
	  .attr("cy", d => yScale(d.rank))
	  .attr("r", circleRadius)
	  .style('opacity', circleOpacity)
	  .style('fill', team_color)
	  .on("mouseover", function(d) {
			d3.select(this)
			  .transition()
			  .duration(duration)
			  .attr("r", circleRadiusHover);
		  })
		.on("mouseout", function(d) {
			d3.select(this) 
			  .transition()
			  .duration(duration)
			  .attr("r", circleRadius);  
		  });
		  
		console.log(team_color);
}

function removeCountryFromLineChart(id)	{
	
	id = id.split(' ').join('_') // United_States_of_America
	
	lines.selectAll('.line-group').attr("id", function(d)	{
			if (d.id == id)	{
				array[d.n_country] = false;
			}
		return d.id;
	});
	
	d3.selectAll('.lines')
	.selectAll('.line-group')
	.select('#' + id)["_groups"][Symbol.unscopables] = {
	copyWithin: false};
	
	d3.selectAll('.lines')
	.selectAll('.line-group')
	.select('#' + id)
	.style('opacity', 0);
	
	removeCircles(id);
}

